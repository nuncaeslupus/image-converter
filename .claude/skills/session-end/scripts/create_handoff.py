#!/usr/bin/env python3
"""Write the handoff.md template to a target path.

Used by the session-end skill (Step 1) when the host repo's CLAUDE.md marker
is `<!-- session-end: handoff=yes -->`. The script writes the template with
placeholders intact; Claude fills the placeholders from conversation context
before staging the file.

By default refuses to overwrite an existing handoff.md unless `--force` is
passed.

Exit codes:
  0 — written
  1 — would overwrite; pass --force to allow
  2 — error
"""

from __future__ import annotations

import argparse
import sys
from datetime import date
from pathlib import Path

TEMPLATE = """# Handoff — <one-line topic / stage name>

## TL;DR

<2-3 sentence summary of the session's outcome and what blocks moving forward>

## State

- Branch: `<branch-name>` (status: <ahead/behind, PR #N if any>)
- Active plan: `<absolute path to plan file, or "none">`
- Last green smoke: `<command + result>`

## DONE this session

- <bullet, with file paths and the why>
- <bullet, with file paths and the why>

## TODO — pick up here

1. <next action, with concrete file:line where applicable>
2. <next action>

## DO NOT TOUCH

- <files/dirs that were approved-as-is this session>
- <upstream artifacts (other PRs in flight, etc.)>

## Reproducible sanity commands

```bash
<verbatim shell line that proves "it works" right now>
```

---
*Generated by session-end on {date}. Replace placeholders before committing.*
"""


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    p.add_argument(
        "--output", default="status/handoff.md", help="target path (default: status/handoff.md)"
    )
    p.add_argument("--force", action="store_true", help="overwrite an existing handoff.md")
    args = p.parse_args()

    target = Path(args.output)
    if target.exists() and not args.force:
        sys.stderr.write(f"{target} exists; pass --force to overwrite\n")
        return 1
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(TEMPLATE.format(date=date.today().isoformat()))
    sys.stderr.write(f"wrote {target}\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
